void main(int s_argc, char *s_argv[])
{
 char s_buffer[512];
           printf("Copies MINZ - Copyright(c)1998-1999 by ChoJaeHyuk\n");
           if(s_argc>=2)
           {
            if(s_argv[1][1]==':')
            {
             g_drive=toupper(s_argv[1][0]);
             if(isalpha(g_drive))
             {
              FILE *s_fr, *s_fw;
              int s_data;
              absread(g_drive-'A', 1, 0L, &s_buffer[0]);
              memcpy(&g_boot[3 + 8], &s_buffer[3 + 8], (0x3e + 9) - (3 + 8));
              abswrite(g_drive-'A', 1, 0L, &g_boot[0]);
              /* sprintf(&g_system_name[0], "%c:\\%s", g_drive, &g_kernel_name[0]); */
              sprintf(&g_system_name[0], "%c:\\%s", g_drive, "MINZ.BIN");
              s_fr=fopen(&g_kernel_name[0], "rb");
              if(s_fr)
              {
               s_fw=fopen(&g_system_name[0], "wb");
               if(s_fw)
               {
                while((s_data=fgetc(s_fr))!=EOF)fputc(s_data, s_fw);
                fclose(s_fw);
                fclose(s_fr);
                printf("\nSystem transfered %c:\n", g_drive);
                return;
               }
               fclose(s_fr);
              }
             }
            }
           }
           printf("Usage: SYS.COM <drive:>\n");
}

